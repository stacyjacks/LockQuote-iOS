import Foundation
import SwiftUI

extension Color {
    static var primaryPink: Color {
        Color("primaryPink")
    }
    
    static var lightGrey: Color {
        Color("lightGrey")
    }
    
    static var darkGrey: Color {
        Color("darkGrey")
    }
    
    static var mediumGrey: Color {
        Color("mediumGrey")
    }
    
    static var lightGreen: Color {
        Color("lightGreen")
    }
}

extension CGFloat {
    /// 8 pt of spacing
    static var XS = 8.0
    
    /// 16 pt of spacing
    static var S = 16.0
    
    /// 24 pt of spacing
    static var M = 24.0
    
    /// 32 pt of spacing
    static var L = 32.0
}
